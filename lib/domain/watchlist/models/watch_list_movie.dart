class WatchlistMovie {
  final int movieId;
  final String title;
  final String genre;
  final double rating;
  final String releaseYear;
  final String posterUrl;
  WatchlistMovie({
    required this.movieId,
    required this.title,
    required this.genre,
    required this.rating,
    required this.releaseYear,
    required this.posterUrl,
  });
}
